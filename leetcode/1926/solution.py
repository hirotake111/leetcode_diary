class Solution:
    def nearestExit(self, maze: List[List[str]], entrance: List[int]) -> int:
        m, n = len(maze), len(maze[0])
        q: Queue[Tuple[int, int, int]] = Queue()
        directions: List[Tuple[int, int]] = [
            (0, -1),
            (1, 0),
            (0, 1),
            (-1, 0),
        ]  # top, right, bottom, left

        # mark the entrance as visited
        maze[entrance[0]][entrance[1]] = "o"

        # for each next direction...
        for direction in directions:
            x, y = direction[0] + entrance[0], direction[1] + entrance[1]
            # add the position and the current steps to the queue if it's a valid path
            if 0 <= x < m and 0 <= y < n and maze[x][y] == ".":
                maze[x][y] = "o"
                q.put((x, y, 0))

        while not q.empty():
            position_x, position_y, cur_steps = q.get()
            # if a or b equals 0, then you reached the nearest exist -> return
            if (
                position_x == 0
                or position_x == m - 1
                or position_y == 0
                or position_y == n - 1
            ):
                return cur_steps + 1
            # else, search other paths
            for direction in directions:
                x, y = position_x + direction[0], position_y + direction[1]
                # add the position and  the current steps to the queue if it's a valid path
                if 0 <= x < m and 0 <= y < n and maze[x][y] == ".":
                    # mark the position as visited
                    maze[x][y] = "o"
                    q.put((x, y, cur_steps + 1))

        return -1


